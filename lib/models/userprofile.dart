class UserProfile {
  String id;
  String name;
  String email;
  String city;
  String state;
  String country;
  String image;
  String createdAt;
  String updatedAt;
  UserProfile({
    required this.id,
    required this.name,
    required this.email,
    required this.city,
    required this.state,
    required this.country,
    required this.image,
    required this.createdAt,
    required this.updatedAt,
  });
}
