class Album {
  String id;
  String title;
  String artist;
  String cover;
  String year;
  String genre;
  String description;
  String link;
  String label;
  String type;
  String release;
  String tracklist;
  String coverBig;
  String coverMedium;
  String coverSmall;
  Album({
    required this.id,
    required this.title,
    required this.artist,
    required this.cover,
    required this.year,
    required this.genre,
    required this.description,
    required this.link,
    required this.label,
    required this.type,
    required this.release,
    required this.tracklist,
    required this.coverBig,
    required this.coverMedium,
    required this.coverSmall,
  });
}
